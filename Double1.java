class Double1
{
        public static void main(String[] Args)
        {
                double MyDouble;
                MyDouble = 1.2345678901 - 1.2345678900;

                System.out.println("MyDouble = " + MyDouble);

                float MyFloat;
                MyFloat = 1.2345678901F - 1.2345678900F;

                System.out.println("MyFloat = " + MyFloat);
        }
}
